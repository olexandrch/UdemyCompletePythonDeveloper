print("dic1['b']: ", dic1['b'])

dic2 = {
    'a':[1,2,3,4,5],
    22:"Hello There",
    'bb':True
}
print("dic2: ",dic2)
print("dic2['a']: ", dic2['a'])
print("dic2['a'][2]: ", dic2['a'][2])


